    public void Break() throws IOException, DocumentException{ // Breaks the pdf file into single page multiple pdfs
        String Path=FileNameField.getText();
        System.out.println("Reading "+Path);
        PdfReader reader=new PdfReader(Path);
        int n=reader.getNumberOfPages();
        System.out.println("No. of the pages in the pdf are: " + n); // These outputs will show up only in the console.
        for (int i=1;i<=n;i++){
            String file="PDF Split_"+i+".pdf";
            Document pdf= new Document(reader.getPageSizeWithRotation(1));
            PdfCopy writer=new PdfCopy(pdf, new FileOutputStream(file));
            pdf.open();
            PdfImportedPage page=writer.getImportedPage(reader, i);
            writer.addPage(page);
            pdf.close();
            writer.close();
        }
        System.out.println("PDF Split Successful");
        OutputLabel.setText("PDF Split was Successful !!!"); // Confirmation of Successful Splitting of PDF
    }   
    
public void break_into() throws IOException, DocumentException{ // extracts the intended (continous) pages and creates a new pdf with those pages
    if("".equals(StartPage.getText()))
         JOptionPane.showMessageDialog(rootPane,"Enter a valid value in the 'From Page' TextField" );
   else if("".equals(EndPage.getText()))
         JOptionPane.showMessageDialog(rootPane,"Enter a valid value in the 'To Page' TextField" );
   else{
            String Path=FileNameField.getText();
            System.out.println("Reading "+Path);
            PdfReader reader=new PdfReader(Path);
            int n=reader.getNumberOfPages();
            int start=Integer.parseInt(StartPage.getText());
            int end=Integer.parseInt(EndPage.getText());
            String file="PDF-Split"+".pdf";
            Document pdf= new Document(reader.getPageSizeWithRotation(1));
            PdfCopy writer=new PdfCopy(pdf, new FileOutputStream(file));
            for(int i=start;i<=end;i++){            
                pdf.open();
                PdfImportedPage page=writer.getImportedPage(reader, i);
                writer.addPage(page);
            }
            pdf.close();
            writer.close();
            System.out.println("PDF Split Successful");
            StartPage.setText("");// To Reset the values
            EndPage.setText("");//    of the TextFields
            OutputLabel.setText("PDF Split was Successful !!!");  // Confirmation of Successful Splitting of PDF
        }
    }
