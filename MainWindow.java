    private void BrowseButtonActionPerformed(java.awt.event.ActionEvent evt) {                                             
        JFileChooser chooser=new JFileChooser();
        FileNameExtensionFilter TextFile=new FileNameExtensionFilter("Only pdf Files (.pdf)", "pdf"); // to ensure only
        chooser.setAcceptAllFileFilterUsed(false);                                                                                //  pdfs are shown to the user
        chooser.addChoosableFileFilter(TextFile);                                                                              //    in the browse dialog box  
        chooser.showOpenDialog(null);                                                                                                 
        File f=chooser.getSelectedFile();
        String filename=f.getAbsolutePath();
        FileNameField.setText(filename);
    }                                            

    private void SplitButtonActionPerformed(java.awt.event.ActionEvent evt) {                                            
        try {
            if(jRadioButton1.isSelected())
                Break();
            else{
                break_into();
            }             
        } catch (IOException ex) {
            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException ex) {
            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
        
        }
    }                                           

    private void jRadioButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                              
        if(jRadioButton1.isSelected()){
            StartPage.setEnabled(false); // To disable the TextFields if the user wants to split every page
            EndPage.setEnabled(false);
        }
        else{
            StartPage.setEnabled(true);
            EndPage.setEnabled(true);        
            }
    }                                             

